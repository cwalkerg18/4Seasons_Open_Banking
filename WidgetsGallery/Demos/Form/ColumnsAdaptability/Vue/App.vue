<template>
  <div>
    <dx-form
      id="form"
      :form-data="employee"
      :col-count-by-screen="colCountByScreen"
      :screen-by-width="screenByWidth"
      :min-col-width="233"
      label-location="top"
      col-count="auto"
    />
    <div class="options">
      <div class="caption">Options</div>
      <div class="option">
        <dx-check-box
          :text="'Set the count of columns regardless of screen size'"
          :value.sync="useColCountByScreen"
        />
      </div>
    </div>
  </div>
</template>
<script>
import { DxCheckBox, DxForm } from 'devextreme-vue';
import employee from './data.js';

export default {
  components: {
    DxCheckBox,
    DxForm
  },
  data() {
    return {
      employee,
      useColCountByScreen: true
    };
  },
  computed: {
    colCountByScreen() {
      return this.useColCountByScreen
        ? {
          sm: 2,
          md: 4
        }
        : null;
    }
  },
  methods: {
    screenByWidth(width) {
      return width < 720 ? 'sm' : 'md';
    }
  }
};
</script>
