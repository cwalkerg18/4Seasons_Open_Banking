<template>
  <dx-tag-box
    :value="conditionInfo.value"
    :items="categories"
    width="auto"
    @value-changed="onValueChanged"
  />
</template>

<script>
import { DxTagBox } from 'devextreme-vue';
import { categories } from './data.js';

export default {
  components: {
    DxTagBox
  },
  props: {
    conditionInfo: {
      type: Object,
      default: () => {}
    }
  },
  data() {
    return {
      categories
    };
  },
  methods: {
    onValueChanged(e) {
      this.conditionInfo.setValue(e.value && e.value.length ? e.value : null);
    }
  }
};
</script>
